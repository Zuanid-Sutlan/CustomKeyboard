        return allEmojis.filter { emoji ->
            emoji.keywords.any { keyword ->
                keyword.contains(searchTerm)
            }
        }
    }

    fun getRecentEmojis(recentEmojiStrings: List<String>): List<EmojiItem> {
        return recentEmojiStrings.mapNotNull { emojiString ->
            allEmojis.find { it.emoji == emojiString }
        }
    }

    fun getEmojisByCategory(category: EmojiCategory): List<EmojiItem> {
        return allEmojis.filter { it.category == category }
    }
